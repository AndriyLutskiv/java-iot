#!/bin/bash
# =============================================================================
# run-console.sh — запуск CrowPi Suite через консольний (текстовий) інтерфейс
# =============================================================================
#
# Запускає crowpi-suite-1.0.0.jar від імені root через 'sudo su -'.
# Root-права необхідні для доступу до GPIO, I2C, SPI, PWM на Raspberry Pi
# (без root Pi4J не може відкрити /dev/mem або /sys/class/gpio).
#
# Використання:
#   ./run-console.sh                         # інтерактивне меню проектів
#   ./run-console.sh --project=p03          # запуск конкретного проекту
#   ./run-console.sh --project=p03 --mock   # mock-режим (без реального GPIO)
#   ./run-console.sh --list                 # список усіх доступних проектів
#   ./run-console.sh --help                 # довідка по прапорах
#
# Збірка JAR (якщо ще не зібраний):
#   JAVA_HOME=/usr/lib/jvm/java-11-openjdk-amd64 ./gradlew shadowJar
# =============================================================================

# Визначаємо абсолютний шлях до директорії, де знаходиться цей скрипт.
# "${BASH_SOURCE[0]}" — шлях до скрипту, навіть якщо він викликаний через symlink.
# cd + pwd — перетворюємо відносний шлях у абсолютний (надійніше ніж dirname).
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# Повний шлях до fat JAR (результат gradle shadowJar у кореневому модулі)
JAR="${SCRIPT_DIR}/build/libs/crowpi-suite-1.0.0.jar"

# ── Перевірка наявності JAR ──────────────────────────────────────────────────
if [ ! -f "$JAR" ]; then
    echo "================================================================="
    echo " ПОМИЛКА: JAR-файл не знайдено:"
    echo "   $JAR"
    echo ""
    echo " Виконайте збірку командою:"
    echo "   JAVA_HOME=/usr/lib/jvm/java-11-openjdk-amd64 ./gradlew shadowJar"
    echo "================================================================="
    exit 1
fi

echo "================================================================="
echo " CrowPi Suite — консольний запуск"
echo " JAR: $JAR"
echo " Аргументи: $*"
echo " Запуск від root через 'sudo su -' ..."
echo "================================================================="

# ── Запуск від root через 'sudo su -' ────────────────────────────────────────
#
# 'sudo su -' відкриває root-оболонку з повним root-середовищем
# (-  означає login shell: завантажує /root/.profile, /root/.bashrc тощо).
#
# Прапор -c "..." передає команду для виконання в цій root-оболонці.
# '$*' розгортається ще у shell користувача і передає всі аргументи скрипту.
#
# Лапки навколо $JAR потрібні на випадок пробілів у шляху.
exec sudo su - -c "java -jar \"${JAR}\" $*"
